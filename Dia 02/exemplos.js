// verificando par ou impar
let numero = 7;

if (numero % 2 === 0) {
    console.log(`${numero} é par.`);
} else {
    console.log(`${numero} é ímpar.`);
}


// classificando notas
let nota = 85;

if (nota >= 90) {
    console.log("Aprovado com A! 🏆");
} else if (nota >= 70) {
    console.log("Aprovado com B.");
} else {
    console.log("Reprovado. 😔");
}
